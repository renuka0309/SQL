public class Renu_logical {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        
        System.out.println(a>b && a!=b);
        System.out.println(a<b && a==10);
        System.out.println(!(a<b));
        System.out.println(!(20<=5));
    }
        
}
